package pl.java.rower.pakiet;

public class Wheel {
	
	private int spokesAmount;
	private int tyreSize;
	private boolean spins;
	
	public int getSpokesAmount() {
		return spokesAmount;
	}

	public void setSpokesAmount(int spokesAmount) {
		this.spokesAmount = spokesAmount;
	}

	public int getTyreSize() {
		return tyreSize;
	}

	public void setTyreSize(int tyreSize) {
		this.tyreSize = tyreSize;
	}

	public boolean isSpins() {
		return spins;
	}

	public void setSpins(boolean spins) {
		this.spins = spins;
	}
	
	
	

}
